#include<iostream>
using namespace std;

int main(){
  int a =5;
  int *p = &a;

  cout<<"the value of a is :"<<a<<endl;
  cout<<"the value of &a is :"<<&a<<endl;
  cout<<"the value of p is :"<<p<<endl;
  cout<<"the value of *p is :"<<*p<<endl;
  cout<<"the value of &p is :"<<&p<<endl;





  return 0;
}